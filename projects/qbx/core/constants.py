# Magic Numbers
MAGIC_SUPERBLOCK = b"QBXVAULT"      # 8 bytes
MAGIC_CHUNK = 0x51425801            # Chunk header
MAGIC_BLOCK = 0x51425803            # Block header  
MAGIC_FOOTER = 0x51425802           # Chunk footer

# Version
VERSION_MAJOR = 1
VERSION_MINOR = 0

# Sizes - estos se calculan basados en las estructuras
SUPERBLOCK_SIZE = 516  # 512 + padding para alignment (real packed size)
SUPERBLOCK_BACKUP_OFFSET = 0x10000  # 64KB

BLOCK_SIZE = 64 * 1024              # 64KB
CHUNK_SIZE = 128 * 1024 * 1024     # 128MB

BLOCKS_PER_CHUNK = CHUNK_SIZE // BLOCK_SIZE  # 2048
OBJECTS_PER_CHUNK = 32768

# Chunk Layout
CHUNK_HEADER_SIZE = 256
CHUNK_BITMAP_SIZE = 16 * 1024       # 16KB
CHUNK_LBA_TABLE_SIZE = BLOCKS_PER_CHUNK * 8  # 16KB (8192 entries × 8 bytes)
CHUNK_OBJECT_TABLE_SIZE = 2 * 1024 * 1024  # 2MB
CHUNK_DATA_OFFSET = 0x50000

# Block Layout
BLOCK_HEADER_SIZE = 64
BLOCK_PAYLOAD_MAX = BLOCK_SIZE - BLOCK_HEADER_SIZE  # 65536 - 64 = 65472

# Offsets
CHUNK_HEADER_OFFSET = 0
CHUNK_BITMAP_OFFSET = 0x100
CHUNK_ROOT_POINTER_OFFSET = 0x4100
CHUNK_EPOCH_OFFSET = 0x4108
CHUNK_LBA_TABLE_OFFSET = 0x20000
CHUNK_OBJECT_TABLE_OFFSET = 0x30000

# Feature Flags
FEAT_COMPRESSION_LZ4 = 0x00000001
FEAT_COMPRESSION_ZSTD = 0x00000002
FEAT_ENCRYPTION = 0x00000004
FEAT_DEDUP = 0x00000008
FEAT_SNAPSHOTS = 0x00000010

# Status
STATUS_CLEAN = 0
STATUS_DIRTY = 1
STATUS_CRASHED = 2
